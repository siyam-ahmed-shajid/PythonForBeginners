#Conditional Statement

good_weather = False
bad_weather = False

if good_weather:
    print("Go to the beach")
elif bad_weather:
    print("Stay at home.")
    print("Do Homework")
else:
    print("Go on a walk")

#Logical Operators

is_hungry = False
is_thirsty = True

if is_hungry and is_thirsty:
    print("Eat and drink something")

if is_hungry or is_thirsty:
    print("Eat and drink something")

if is_thirsty and not is_hungry:
    print("Drink something but do not eat")

